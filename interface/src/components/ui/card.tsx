import * as React from "react";
import { cn } from "@/lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "elevated" | "bordered" | "interactive";
  }
>(({ className, variant = "default", ...props }, ref) => {
  const variants = {
    default: "bg-[var(--background-surface)] border border-[var(--border)]",
    elevated:
      "bg-[var(--background-elevated)] border border-[var(--border-subtle)] shadow-lg shadow-black/20",
    bordered: "bg-transparent border border-[var(--border-strong)]",
    interactive:
      "bg-[var(--background-surface)] border border-[var(--border)] hover:border-[var(--border-strong)] hover:bg-[var(--background-subtle)] transition-all duration-150 cursor-pointer",
  };

  return (
    <div
      ref={ref}
      className={cn("rounded-lg", variants[variant], className)}
      {...props}
    />
  );
});
Card.displayName = "Card";

const CardHeader = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn("flex flex-col space-y-1.5 p-4 pb-2", className)}
    {...props}
  />
));
CardHeader.displayName = "CardHeader";

